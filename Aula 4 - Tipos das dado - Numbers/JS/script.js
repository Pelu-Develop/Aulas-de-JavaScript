var numero = 5;

console.log(numero);

/* typeof confere que tipo de dado é a variavel, nesse caso estar verificando que é um dado estilo numero */
console.log(typeof numero);

var float = 5.32;

console.log(float);

/* o tipo de dado, mesmo em numero decimal é numero */
console.log(typeof float);

/* Quando colocamos qualquer dado da varialvel entre "", o dado gerado sempre será um texto/string */
var textoComNumero = "523"

console.log(textoComNumero);

/* A variavel gerada é um string/um texto  */
console.log(typeof textoComNumero);

/* Todos os tipos abaixo são considerados um numbers, são valores simbolicos */
console.log(NaN);
console.log(typeof NaN);
console.log(typeof +Infinity);
console.log(typeof -Infinity);