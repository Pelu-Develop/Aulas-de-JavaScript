/* Boolean representa um valor verdadeiro ou falso */
/* Comparações resultam em booleans */
/* Podemos tambem atribuir um true ou false para uma variavel e ela terá o tipo de Boolean */

/* Declarando uma variavel Boolean */
var verdadeiro = true;

console.log(verdadeiro);
console.log(typeof verdadeiro);

/* Tipos de dados Boolean */
console.log(typeof true);
console.log(typeof false);

var falso = false;

console.log(falso);
console.log(typeof falso);
